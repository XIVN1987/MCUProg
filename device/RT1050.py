from .flash import Flash


class RT1050(object):
    CHIP_CORE = 'Cortex-M7'

    PAGE_SIZE =  256
    SECT_SIZE = 1024 * 4
    CHIP_SIZE = 0x2000000   # 32MByte

    def __init__(self, jlink):
        super(RT1050, self).__init__()
        
        self.jlink  = jlink

        self.flash = Flash(self.jlink, RT1050_flash_algo)
    
    def sect_erase(self, addr, size):
        self.flash.Init(0, 0, 1)
        for i in range(0, (size + self.SECT_SIZE - 1)//self.SECT_SIZE):
            self.flash.EraseSector(0x60000000 + addr + self.SECT_SIZE * i)
        self.flash.UnInit(1)

    def chip_write(self, addr, data):
        data = data + [0xFF] * (self.PAGE_SIZE - len(data)%self.PAGE_SIZE)

        self.sect_erase(addr, len(data))

        self.flash.Init(0, 0, 2)
        for i in range(0, len(data)//self.PAGE_SIZE):
            self.flash.ProgramPage(0x60000000 + addr + self.PAGE_SIZE * i, data[self.PAGE_SIZE*i : self.PAGE_SIZE*(i+1)])
        self.flash.UnInit(2)
        
    def chip_read(self, addr, size, buff):
        self.jlink.go()     # 必须得运行中才能读出
        data = self.jlink.read_mem(0x60000000 + addr, size)

        buff.extend([ord(x) for x in data])


